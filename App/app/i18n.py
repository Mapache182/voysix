class Translator:
    def __init__(self, lang="en"):
        self.lang = lang if lang in TRANSLATIONS else "en"
    
    def translate(self, key):
        return TRANSLATIONS[self.lang].get(key, key)
    
    def get_help(self, key):
        return HELP_TEXT[self.lang].get(key, "No info available")

# Global translator instance (will be updated when config moves)
_t = Translator("en")

def set_ui_lang(lang):
    global _t
    _t = Translator(lang)

def tr(key):
    return _t.translate(key)

def hlp(key):
    return _t.get_help(key)
